class Nodo():
    elemento = null
    proximo = null

    def __init__(self,elemento):
        self.elemento = elemento
        self.proximo = proximo
    
    def setElemento(e):
        elemento = e
    def setProximo(n):
        proximo = n 
    def getElemento():
        return elemento
    def getProximo():
        return proximo
    